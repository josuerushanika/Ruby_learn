In short, a class is the definition of an object (attributes, methods, etc) and an object is 
an instance of a class (a variable of the type of that class).

The four principles of OOP are:

Encapsulation: An object is attributes can only be accessed/modified by one of its methods.
Abstraction: Show only the things needed.
Inheritance: The ability for one object to acquire properties from another object.
Polymorphism: The ability to have many implementations of a single interface.


Composition vs inheritance
Instead of having an object that inherits attributes 
and methods you can compose an object that has other 
smaller objects inside it and uses their 
attributes and methods. This is called composition.